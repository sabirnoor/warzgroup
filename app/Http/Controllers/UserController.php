	public function index(Request $request) {
		if ($request->isMethod('post')) {
			if ($request->user_type != 'm') {
				$data = array('status' => false, 'message' => 'Invalid user');
				return response()->json($data);
			}
			$user = UserModel::where('email', $request->email)
				->where('password', md5($request->password))->where('user_type', 'm')->first();
			if ($user) {
				$data = array('status' => true, 'message' => 'success', 'user' => $user);
				return response()->json($data);
			} else {
				$data = array('status' => false, 'message' => 'Invalid username or password');
				return response()->json($data);
			}
		} else {
			$data = array('status' => false, 'message' => 'Invalid request');
			return response()->json($data);
		}

		//print_r($user);die('ssss');
		//return response()->json(UserModel::limit(10)->get());
	}

